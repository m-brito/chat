    //     controller.add(lista);
    //   });
    // });
  }

  @override
  Future<ChatMessage?> save(String text, TypeMessage type, ChatUser user) async {
    final store = FirebaseFirestore.instance;

    final msg = ChatMessage(
      id: '',
      text: text,
      createdAt: DateTime.now(),
      userId: user.id,
      userName: user.name,
      userImageUrl: user.imageUrl,
      type: type,
    );

    final docRef = await store
        .collection('chat')
        .withConverter(
          fromFirestore: _fromFirestore,
          toFirestore: _toFirestore,
        )
        .add(msg);

    final doc = await docRef.get();
    return doc.data()!;
  }

  @override
  Future<dynamic> delete(id) async {
    final store = FirebaseFirestore.instance;
    try{
      final resp = await store.collection('chat').doc(id).delete();
      return 'Mensagem deletada';
    }catch(error) {
      throw HttpException(msg: 'Erro ao deletar mensagem');
    }
  }

  // ChatMessage => Map<String, dynamic>
  Map<String, dynamic> _toFirestore(
    ChatMessage msg,
    SetOptions? options,
  ) {
    return {
      'text': msg.text,
      'createdAt': msg.createdAt.toIso8601String(),
      'userId': msg.userId,
      'userName': msg.userName,
      'userImageUrl': msg.userImageUrl,
      'type': ChatMessage.typeMessageToString(msg.type),
    };
  }

  // Map<String, dynamic> => ChatMessage
  ChatMessage _fromFirestore(
    DocumentSnapshot<Map<String, dynamic>> doc,
    SnapshotOptions? options,
  ) {
    print('type ${doc["type"]}, ${doc["text"]}, ${TypeMessage.Text}');
    return ChatMessage(
      id: doc.id,
      text: doc['text'],
      createdAt: DateTime.parse(doc['createdAt']),
      userId: doc['userId'],
      userName: doc['userName'],
      userImageUrl: doc['userImageUrl'],
      type: ChatMessage.stringToTypeMessage(doc['type']),
    );
  }
}
